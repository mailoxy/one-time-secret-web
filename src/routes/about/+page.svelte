		<h1 class="text-3xl font-bold">One Time Secret</h1>

		<h2 class="text-2xl font-bold">About One Time Secret</h2>
		<p class="text-gray-500">
			One Time Secret is a web application designed to share sensitive information securely and
			confidentially. The aim is to provide an easy-to-use service that safeguards your information
			by using cutting-edge encryption technology.
		</p>
		<h3 class="text-xl font-bold">How It Works</h3>
		<p class="text-gray-500">
			When you need to share sensitive information, such as passwords or other confidential
			information, you can use One Time Secret to encrypt and share that information. This ensures
			that only the intended recipient can access the information, and that it's deleted immediately
			after it's accessed.
		</p>

		<p class="text-gray-500">
			Instead of sending the information directly, you input it into One Time Secret, which then
			encrypt the data in your browser using a AES 256-bit encryption algorithm, ensuring that the
			raw data never leaves your computer unencrypted. Once the data is encrypted, you will be
			provided with an unique, one-time-use URL. This URL can be shared with the recipient of your
			choice. When they access the URL, the secret is decrypted in their browser, and then it's
			immediately deleted from our servers.
		</p>

		<p class="text-gray-500">
			One Time Secret's code is publicly available. This allows anyone to review the project code.
